package com.bucles.forloop;

public class ForLoops {
    public static void main(String[] args) {
        /* inicizamos a i en uno;
            incrementa en uno y y ejecuta lo que este dentro
            cuando i valga 6 para el ciclo sin entrar
         */

        for (int i = 10; i > 4; i--){
            System.out.println("Hola voy por el "+i);
        }
    }
}
